import { createServerClient } from '@supabase/ssr';
import { createClient as createSupabaseClient } from '@supabase/supabase-js';
import { cookies } from 'next/headers';

// Cookie-based server client — use in Server Components, Route Handlers, and Middleware.
// Session is read from the incoming request cookies. Never import this in 'use client' files.
export function createClient() {
  const cookieStore = cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // Called from a Server Component — cookies can't be set.
            // Middleware handles session refresh; this is safe to ignore.
          }
        },
      },
    }
  );
}

// Service-role client — bypasses RLS. Use only in trusted Route Handlers
// (e.g. /api/share). SUPABASE_SERVICE_ROLE_KEY must never be NEXT_PUBLIC_.
export function createServiceClient() {
  return createSupabaseClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    { auth: { persistSession: false } }
  );
}
